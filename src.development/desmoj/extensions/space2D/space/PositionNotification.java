package desmoj.extensions.space2D.space;

import desmoj.core.simulator.Entity;
import desmoj.core.simulator.SimTime;

/**
 * PositionNotifications denote arrivals at a position for agents. These
 * notifications are processed by the environment: it sends a PositionReached
 * signal to the agent.
 * 
 * @author Ruth Meyer
 */
public class PositionNotification extends EnvironmentSignal {

	/**
	 * constructs a new position notification event for the given agent.
	 * 
	 * @param time
	 *            the point in simulation time when the arrival is due
	 * @param entity
	 *            the agent to be notified about the arrival
	 */
	public PositionNotification(SimTime time, Entity entity) {
		super(time, entity);
	}
}